#include <iostream>
#include <cstdlib>
#include <cmath>

using namespace std;

int main()
{
    //Изменение кодировки консоли:
    system("chcp 1251 > nul");
    //Индекс последнего слогаемого в сумме:
    int n = 100;
    //Аргумент для экспоненты:
    double x = 1;
    /*Переменная для записи значения суммы, переменная
        для записи значения добавки и индексная переменная:*/
    double s = 0, q = 1, k = 0;
    //Оператор цыкла для вычисления суммы:
    do {
        //Прибавление к сумме добавки:
        s += q;
        //Новое значение индексной пременной:
        k++;
        //Новое значение для добавки:
        q *= x / k;
    } while (k <= n);
    //Отображение результата вычисления:
    cout << "Вычисленное значение: " << s << endl;
    /*Использование встроенной функции для вычисления
    значения экспоненты:*/
    cout << "Контрольное значение: " << exp(x) << endl;
    //Задержка консольного окна:
    system("pause > nul");
    return 0;
}
